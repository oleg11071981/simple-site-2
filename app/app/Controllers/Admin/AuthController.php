<?php

/**
 * Контроллер авторизации для административной панели
 *
 * Управляет процессами входа, выхода и аутентификации пользователей
 * в административной части сайта.
 *
 * @package App\Controllers\Admin
 * @category Controllers
 * @license MIT
 * @link    http://localhost/admin-panel
 * @noinspection PhpUnused
 */

namespace App\Controllers\Admin;

use App\Controllers\BaseController;
use App\Models\NUsersModel;
use CodeIgniter\HTTP\RedirectResponse;
use ReflectionException;

/**
 * Контроллер авторизации для административной панели
 *
 * Обрабатывает запросы, связанные с аутентификацией пользователей:
 * - Отображение формы входа
 * - Проверка учетных данных
 * - Управление сессией
 * - Выход из системы
 *
 * @package App\Controllers\Admin
 */
class AuthController extends BaseController
{
    /**
     * Модель пользователей
     *
     * @var NUsersModel Модель для работы с таблицей n_users
     */
    protected NUsersModel $userModel;

    /**
     * Конструктор контроллера
     *
     * Инициализирует необходимые модели и сервисы
     * для работы контроллера авторизации.
     */
    public function __construct()
    {
        // Инициализация модели пользователей
        $this->userModel = new NUsersModel();
    }

    /**
     * Отображение страницы авторизации
     *
     * Проверяет, авторизован ли пользователь. Если да - перенаправляет
     * в панель управления. Если нет - показывает форму входа.
     *
     * @route GET /admin-panel/login
     *
     * @return string|RedirectResponse HTML страница входа или редирект на дашборд
     */
    public function login()
    {
        // Проверяем, авторизован ли уже пользователь
        if (session()->get('is_logged_in')) {
            // Если авторизован - отправляем в панель управления
            return redirect()->to('/admin-panel/dashboard');
        }

        // Показываем страницу входа
        return view('admin/auth/login');
    }

    /**
     * Обработка данных формы авторизации
     *
     * Получает данные из POST-запроса, валидирует их,
     * проверяет учетные данные и создает сессию при успешном входе.
     *
     * @route POST /admin-panel/auth/authenticate
     *
     * @return RedirectResponse Редирект на дашборд или обратно на форму входа
     * @throws ReflectionException
     */
    public function authenticate(): RedirectResponse
    {
        // ============================================================
        // Шаг 1: Получение данных из формы
        // ============================================================

        /** @var string|null $login Логин или email пользователя */
        $login = $this->request->getPost('login');

        /** @var string|null $password Пароль пользователя */
        $password = $this->request->getPost('password');

        // ============================================================
        // Шаг 2: Валидация входных данных
        // ============================================================

        // Проверяем, что оба поля заполнены
        if (empty($login) || empty($password)) {
            // Возвращаем ошибку с сохранением введенных данных
            return redirect()->back()
                ->with('error', 'Пожалуйста, заполните все поля')
                ->withInput();
        }

        // ============================================================
        // Шаг 3: Поиск пользователя в базе данных
        // ============================================================

        /**
         * Поиск активного пользователя по логину или email
         *
         * @var array|null $user Данные пользователя или null если не найден
         */
        $user = $this->userModel
            ->groupStart()
                ->where('login', $login)
                ->orWhere('email', $login)
            ->groupEnd()
            ->where('publish', 1)
            ->first();

        // ============================================================
        // Шаг 4: Проверка пароля и авторизация
        // ============================================================

        // Проверяем существование пользователя и корректность пароля
        if ($user && $this->userModel->verifyPassword($password, $user['password'])) {

            // --------------------------------------------------------
            // 4.1: Создание сессии пользователя
            // --------------------------------------------------------

            $sessionData = [
                'user_id'       => $user['id'],          // ID пользователя
                'user_login'    => $user['login'],       // Логин
                'user_name'     => $user['name'],        // Имя пользователя
                'user_type'     => $user['type'],        // Тип (0-обычный,1-админ)
                'user_email'    => $user['email'],       // Email
                'is_logged_in'  => true,                 // Флаг авторизации
                'logged_in_at'  => date('Y-m-d H:i:s')   // Время входа
            ];

            // Сохраняем данные в сессию
            session()->set($sessionData);

            // --------------------------------------------------------
            // 4.2: Обновление времени последнего входа
            // --------------------------------------------------------

            $updateData = [
                'modify'         => date('Y-m-d H:i:s'),     // Время изменения
                'modify_by_user' => $user['id']              // Кто изменил (сам пользователь)
            ];

            // Обновляем запись в базе данных
            $this->userModel->update($user['id'], $updateData);

            // --------------------------------------------------------
            // 4.3: Логирование успешного входа
            // --------------------------------------------------------

            /**
             * Записываем в лог информацию об успешной авторизации
             *
             * @see https://codeigniter.com/user_guide/general/logging.html
             */
            log_message('info', '[AUTH] Пользователь "{login}" (ID: {id}) успешно вошел в систему', [
                'login' => $user['login'],
                'id'    => $user['id']
            ]);

            // Перенаправляем в панель управления (без уведомления)
            return redirect()->to('/admin-panel/dashboard');
        }

        // ============================================================
        // Шаг 5: Обработка неудачной попытки входа
        // ============================================================

        /**
         * Записываем в лог предупреждение о неудачной попытке
         * Это помогает отслеживать подбор паролей
         */
        log_message('warning', '[AUTH] Неудачная попытка входа для логина/email: {login} с IP: {ip}', [
            'login' => $login,
            'ip'    => $this->request->getIPAddress()
        ]);

        // Возвращаем на страницу входа с сообщением об ошибке
        return redirect()->back()
            ->with('error', 'Неверный логин или пароль')
            ->withInput();  // Сохраняем введенный логин для удобства пользователя
    }

    /**
     * Выход пользователя из системы
     *
     * Завершает сессию пользователя, очищает все данные
     * и перенаправляет на страницу входа.
     *
     * @route GET /admin-panel/logout
     *
     * @return RedirectResponse Редирект на страницу входа
     */
    public function logout(): RedirectResponse
    {
        // ============================================================
        // Шаг 1: Получение данных пользователя для лога
        // ============================================================

        /** @var int|null $userId ID пользователя из сессии */
        $userId = session()->get('user_id');

        /** @var string|null $userLogin Логин пользователя из сессии */
        $userLogin = session()->get('user_login');

        // ============================================================
        // Шаг 2: Логирование выхода из системы
        // ============================================================

        /**
         * Записываем в лог информацию о выходе пользователя
         * Это важно для аудита действий в системе
         */
        log_message('info', '[AUTH] Пользователь "{login}" (ID: {id}) вышел из системы', [
            'login' => $userLogin,
            'id'    => $userId
        ]);

        // ============================================================
        // Шаг 3: Очистка сессии
        // ============================================================

        /**
         * Уничтожаем все данные сессии
         * session()->destroy() полностью удаляет сессию,
         * в отличие от session()->remove() или session()->reset()
         */
        session()->destroy();

        // ============================================================
        // Шаг 4: Перенаправление на страницу входа
        // ============================================================

        // Возвращаем пользователя на страницу авторизации с сообщением
        return redirect()->to('/admin-panel/login')
            ->with('success', 'Вы вышли из системы');
    }
}